package proyect;

/**
 *
 * @author jose
 */
public class empleados {
    String nombre;
    int identidad;
    int edad;
    String direccion;

    public empleados() {
    }

    public empleados(String nombre, int identidad, int edad, String direccion) {
        this.nombre = nombre;
        this.identidad = identidad;
        this.edad = edad;
        this.direccion = direccion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getIdentidad() {
        return identidad;
    }

    public void setIdentidad(int identidad) {
        this.identidad = identidad;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    @Override
    public String toString() {
        return  nombre ;
    }
    
}

